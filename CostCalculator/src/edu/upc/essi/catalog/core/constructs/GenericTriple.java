package edu.upc.essi.catalog.core.constructs;

public class GenericTriple<T1, T2, T3> {
	
	private T1 val1;
	private T2 val2;
	private T3 val3;
	
	public T1 getVal1() {
		return val1;
	}
	public void setVal1(T1 val1) {
		this.val1 = val1;
	}
	public T2 getVal2() {
		return val2;
	}
	public void setVal2(T2 val2) {
		this.val2 = val2;
	}
	public T3 getVal3() {
		return val3;
	}
	public void setVal3(T3 val3) {
		this.val3 = val3;
	}
	
	
	
	
}
